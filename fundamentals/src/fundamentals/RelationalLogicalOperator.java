package fundamentals;

public class RelationalLogicalOperator {
public static void main(String[] args) {
	
	int a=11;
	double b=11.0;
	System.out.println(a>b);
	System.out.println(a<b);
	System.out.println(a==b);
	System.out.println(a!=b);
	System.out.println(a>-b);
	System.out.println(a<=b);
	
	System.out.println(true && true);
	System.out.println(true&&!false);
	System.out.println(55.5%10);
	
	
}
}
